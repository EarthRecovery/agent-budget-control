  bash scripts/budget-estimation-benchmark/4-qwen3-32b-smoke.sh [target...]

Behavior:
  - No positional args: run only `sokoban_instant` as a smoke test.
  - Reuses `4-qwen3-32b.sh` but overrides it with smaller, safer defaults.
  - Default output dir: `results/budget-estimation-benchmark-smoke`

Examples:
  bash scripts/budget-estimation-benchmark/4-qwen3-32b-smoke.sh
  bash scripts/budget-estimation-benchmark/4-qwen3-32b-smoke.sh gpqa_main_instant
  DRY_RUN=1 bash scripts/budget-estimation-benchmark/4-qwen3-32b-smoke.sh gpqa_main_thinking
EOF
}

