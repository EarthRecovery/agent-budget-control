find_nvcc() {
  if [[ -n "${CUDA_HOME:-}" && -x "${CUDA_HOME}/bin/nvcc" ]]; then
    printf '%s\n' "${CUDA_HOME}/bin/nvcc"
    return 0
  fi
  command -v nvcc 2>/dev/null || true
}

configure_vllm_sampler() {
  if [[ -n "${VLLM_USE_FLASHINFER_SAMPLER:-}" ]]; then
    echo "    VLLM_USE_FLASHINFER_SAMPLER=${VLLM_USE_FLASHINFER_SAMPLER} (preset)"
    return 0
  fi

  local nvcc_path
  nvcc_path=$(find_nvcc)
  if [[ -n "$nvcc_path" ]]; then
    echo "    nvcc=${nvcc_path}"
    return 0
  fi

  export VLLM_USE_FLASHINFER_SAMPLER=0
  echo "    nvcc not found; setting VLLM_USE_FLASHINFER_SAMPLER=0"
}

usage() {
  cat <<'EOF'
